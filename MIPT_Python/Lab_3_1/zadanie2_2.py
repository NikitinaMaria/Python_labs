import turtle
import numpy as np
turtle.shape('turtle')

def ris(pug1, px, pug2, x1, ug1, x2, ug2, x3, ug3, x4, ug4, x5, ug5, x6, kug1, kx1, kug2, kx2):
    turtle.right(pug1)
    turtle.forward(px)
    turtle.right(pug2)
    turtle.pendown()
    turtle.forward(x1)
    turtle.right(ug1)
    turtle.forward(x2)
    turtle.right(ug2)
    turtle.forward(x3)
    turtle.right(ug3)
    turtle.forward(x4)
    turtle.right(ug4)
    turtle.forward(x5)
    turtle.right(ug5)
    turtle.forward(x6)
    turtle.penup()
    turtle.right(kug1)
    turtle.forward(kx1)
    turtle.right(kug2)
    turtle.forward(kx2)

ris0 = (0, 0, 0, 20, 90, 40, 90, 20, 90, 40, 0, 0, 0, 0, 90, 40, 0, 0)
